void	ft_rev_int_tab(int *tab, int size)
{
	int	tab2[size];
	int	i;

	i = 0;
	while (i < size)
	{
		tab2[i] = tab[i];
		i++;
	}
	i = 0;
	while (size > 0)
	{
		size--;
		tab[i] = tab2[size];
		i++;
	}
}
