#include <unistd.h>

void	print_matriz(char m[4][4])
{
	int	i;
	int	j;
	int k;

	i = 0;
	j = 0;
	k = 0;
	while (i < 4)
	{
		while (j < 4)
		{
			write(1, &m[i][j], 1);
			while (k < 3)
			{
				write(1, " ", 1);
				k++;
			}
			j++;
			k = 0;
		}
		write(1, "\n", 1);
		j = 0;
		i++;
	}
}
